# ── Pipeline ──────────────────────────────────────────────────────────────────

async def run_pipeline(topic: str, days: Optional[int] = None):
    print(f"\n🚀 파이프라인 시작: '{topic}'  ({days_to_label(days)})")
    print()

    print("1️⃣  캐시 확인 중...")
    cached       = find_similar_query(topic)
    canonical_id = cached["query_id"] if cached else None
    since_date   = cached["last_run"] if (cached and not days) else None
    if not cached:
        print("   캐시 없음 — 전체 수집 시작")
    elif days:
        print(f"   캐시 있음 — 날짜 필터({days_to_label(days)}) 우선 적용")
    else:
        print(f"   캐시 히트 — 마지막 검색({cached['last_run'][:10]}) 이후 결과만 가져옵니다")

    print("\n2️⃣  데이터 수집 중 (Scholar + X + Threads 동시)...")
    raw    = await scrape_all(topic, days=days)
    counts = {k: len(v) for k, v in raw.items()}
    parts  = []
    for src, label in [("scholar","Scholar"),("x","X"),("threads","Threads"),("web","Web")]:
        n = counts[src]
        parts.append(f"{label} {n}건" if n else f"{label} —")
    print(f"   수집 완료: {' / '.join(parts)}")

    if not any(counts.values()):
        print("   ⚠️  모든 소스에서 결과를 찾지 못했습니다. 주제를 바꿔 다시 시도해보세요.")
        return

    print("\n3️⃣  의미론적 랭킹 중...")
    all_flat = [item for src in ["scholar", "x", "threads", "web"] for item in raw.get(src, [])]
    ranked   = rank_by_relevance(topic, all_flat)
    split = {
        "scholar": [r for r in ranked if r["source"] == "scholar"],
        "x":       [r for r in ranked if r["source"] == "x"],
        "threads": [r for r in ranked if r["source"] == "threads"],
        "web":     [r for r in ranked if r["source"] == "web"],
    }

    print("\n4️⃣  신뢰도 평가 중...")
    split = score_all_reliability(split)
    tiers = {"reliable": 0, "uncertain": 0, "unreliable": 0}
    for items in split.values():
        for item in items:
            tiers[item["reliability_tier"]] += 1
    print(f"   🟢 신뢰 {tiers['reliable']}건  🟡 불확실 {tiers['uncertain']}건  🔴 비신뢰 {tiers['unreliable']}건")

    print("\n5️⃣  중복 제거 중...")
    split = deduplicate(split)

    print("\n6️⃣  긴 초록 요약 중...")
    split = summarize_abstracts(split)

    if cached:
        seen_urls = {r["url"] for r in cached["results"]}
        for src in ["scholar", "x", "threads", "web"]:
            for item in split.get(src, []):
                item["is_new"] = item["url"] not in seen_urls

    print("\n7️⃣  캐시 저장 중...")
    qid = save_to_cache(topic, split, canonical_id=canonical_id)
    print(f"   저장 완료 (query_id={qid})")

    meta = {"is_cached": bool(cached), "last_run": cached["last_run"] if cached else None}
    print_results(topic, days, split, meta)



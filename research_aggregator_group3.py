"""## 셀 4 — 신뢰도 평가 (Reliability Scorer)

각 결과에 0.0 ~ 1.0 점수를 매기고 🟢 신뢰 / 🟡 불확실 / 🔴 비신뢰 등급을 부여합니다.

### X / Threads 게시물의 특별 처리

X나 Threads 게시물은 게시물 자체(x.com)의 도메인이 아니라
**게시물 안에 링크된 논문/저널의 도메인**으로 신뢰도를 평가합니다.

```
X 게시물 → x.com          → 도메인 점수 낮음 (0.05)
               ↓ paper_url
           → nature.com   → 도메인 점수 높음 (0.40) ← 이걸 씁니다
```

nature.com을 링크한 트윗은 랜덤 X 게시물보다 훨씬 높은 신뢰도를 받습니다.

### 전체 채점 기준

| 항목 | 최대 점수 | 비고 |
|------|-----------|------|
| 도메인 권위 | 0.40 | X/Threads는 paper_url 도메인 기준 |
| 인용 수 | 0.20 | 500회 이상이면 만점 |
| 학술지/학회 명성 | 0.20 | NeurIPS, Nature, IEEE 등 |
| 콘텐츠 일관성 | 0.15 | 충분히 긴 설명, 이모지 남발 없음 |
| 부정 신호 패널티 | -0.40 | 음모론, 비속어 등 |

### 등급
🟢 **신뢰**: ≥ 0.70 &nbsp;|&nbsp; 🟡 **불확실**: 0.40–0.69 &nbsp;|&nbsp; 🔴 **비신뢰**: < 0.40

"""

import re
from urllib.parse import urlparse

RELIABLE_DOMAINS = {
    # Core academic repositories
    "nature.com", "science.org", "cell.com", "thelancet.com",
    "nejm.org", "bmj.com", "arxiv.org", "biorxiv.org", "medrxiv.org",
    "scholar.google.com", "semanticscholar.org", "pubmed.ncbi.nlm.nih.gov",
    "ieeexplore.ieee.org", "dl.acm.org", "openreview.net",
    "proceedings.mlr.press", "jmlr.org", "aaai.org",
    # Major academic publishers
    "sciencedirect.com", "springer.com", "springerlink.com",
    "wiley.com", "onlinelibrary.wiley.com",
    "jstor.org", "tandfonline.com", "sagepub.com",
    "oxfordacademic.com", "academic.oup.com",
    "cambridge.org", "press.princeton.edu",
    "plos.org", "plosone.org", "ploscompbiol.org", "plosmedicine.org",
    "frontiersin.org", "mdpi.com", "hindawi.com",
    "royalsocietypublishing.org", "pnas.org", "science.sciencemag.org",
    "jamanetwork.com", "acpjournals.org", "ahajournals.org",
    "diabetesjournals.org", "bloodjournal.org", "asnjournals.org",
    "researchgate.net", "academia.edu",
    # Preprint and open access
    "ssrn.com", "osf.io", "zenodo.org", "figshare.com",
    # University presses and repositories
    "mit.edu", "stanford.edu", "harvard.edu", "berkeley.edu",
    "caltech.edu", "cmu.edu", "ox.ac.uk", "cam.ac.uk",
}

# University domain pattern — any .edu or .ac.uk/.ac.jp etc.
_UNI_PATTERN = re.compile(r'\.(edu|ac\.[a-z]{2}|edu\.[a-z]{2})$')

RELIABLE_VENUES = {
    "neurips", "icml", "iclr", "cvpr", "acl", "emnlp",
    "nature", "science", "cell", "lancet", "nejm", "bmj", "ieee", "acm",
    "pnas", "plos", "jama", "aha", "annals",
}

UNRELIABLE_SIGNALS = [
    r"\b(conspiracy|fake|hoax|debunked|pseudoscience)\b",
    r"\b(lol|omg|wtf|bruh)\b",
    r"🔥{3,}",
]


def _domain_score(url: str) -> float:
    """Return the domain authority score (0–0.40) for a given URL."""
    try:
        domain = urlparse(url).netloc.replace("www.", "")
        if domain in RELIABLE_DOMAINS:      return 0.40
        if _UNI_PATTERN.search(domain):     return 0.38
        if domain.endswith(".gov"):         return 0.35
        if domain.endswith(".org"):         return 0.15
    except Exception:
        pass
    return 0.0


def score_reliability(result: dict) -> dict:
    """단일 결과에 신뢰도 점수와 등급을 부여합니다."""
    score     = 0.0
    url       = result.get("url", "")
    paper_url = result.get("paper_url", "")
    snippet   = result.get("snippet", "").lower()
    venue     = result.get("venue", "").lower()
    citations = result.get("citations", 0) or 0
    source    = result.get("source", "")

    # 1) 도메인 권위 (0 ~ 0.40)
    # For X and Threads: score the paper_url they link to, not the post URL.
    # A tweet linking to nature.com is far more credible than x.com itself.
    # Fall back to the post URL score if no paper_url.
    if source in ("x", "threads"):
        paper_score = _domain_score(paper_url) if paper_url else 0.0
        post_score  = 0.05   # base credit for being a social post
        score += max(paper_score, post_score)

        # Also check paper domain against venue list
        if paper_url:
            paper_domain = urlparse(paper_url).netloc.replace("www.", "").lower()
            for v in RELIABLE_VENUES:
                if v in paper_domain:
                    score += 0.20
                    break
    else:
        score += _domain_score(url)

    # 2) 인용 수 (0 ~ 0.20)
    if   citations >= 500: score += 0.20
    elif citations >= 100: score += 0.15
    elif citations >= 20:  score += 0.10
    elif citations >= 5:   score += 0.05

    # 3) 학술지/학회 명성 — from venue field (Scholar results)
    if source not in ("x", "threads"):
        for v in RELIABLE_VENUES:
            if v in venue:
                score += 0.20
                break

    # 4) 콘텐츠 일관성 (0 ~ 0.15)
    if   len(snippet) > 150: score += 0.10
    elif len(snippet) > 60:  score += 0.05
    if len(re.findall(r"[^\w\s,.]", snippet)) < 3:
        score += 0.05

    # 5) 부정 신호 패널티 (최대 -0.40)
    for pattern in UNRELIABLE_SIGNALS:
        if re.search(pattern, snippet, re.IGNORECASE):
            score -= 0.20

    # 6) 소스별 조정
    if source == "scholar":
        score = min(score + 0.10, 1.0)

    score = round(max(0.0, min(score, 1.0)), 3)

    result["reliability_score"] = score
    result["reliability_tier"]  = (
        "reliable"   if score >= 0.70 else
        "uncertain"  if score >= 0.40 else
        "unreliable"
    )
    return result


def score_all_reliability(results: dict) -> dict:
    """모든 소스의 결과에 신뢰도 점수를 적용합니다."""
    for source in ["scholar", "x", "threads"]:
        results[source] = [score_reliability(r) for r in results.get(source, [])]
    return results


print("✅ 신뢰도 평가기 정의 완료")
print("   사용 가능한 함수: score_reliability(result), score_all_reliability(results)")

"""## 셀 5 — 중복 제거 (Cross-Platform Deduplicator)

여러 플랫폼에서 같은 내용이 중복 수집되는 경우를 제거합니다.

예: 어떤 NeurIPS 논문이 Scholar에서도 뜨고 X에서도 누군가 공유했다면 하나만 남깁니다.

### 중복 판정 방법

**1단계 — URL 완전 일치**: 같은 URL이면 즉시 중복 처리

**2단계 — 제목 자카드 유사도**: 두 제목의 단어 집합이 75% 이상 겹치면 중복 처리
```
제목 A: {advances, in, llm, alignment}      (4 단어)
제목 B: {recent, advances, llm, alignment}  (4 단어)
공통:   {advances, llm, alignment}          → 3개
전체:   {advances, in, llm, alignment, recent} → 5개
자카드: 3/5 = 0.60  →  임계값 0.75 미만이므로 중복 아님
```

중복 발견 시 **신뢰도 점수가 더 높은 버전**을 남깁니다.

"""

def deduplicate(results: dict) -> dict:
    """
    Scholar / X / Threads 결과에서 중복을 제거합니다.
    중복 중 신뢰도 점수가 높은 것을 남깁니다.
    """
    all_items = [
        item
        for source in ["scholar", "x", "threads", "web"]
        for item in results.get(source, [])
    ]

    # 신뢰도가 높은 것이 먼저 처리되어 살아남도록 내림차순 정렬
    all_items.sort(key=lambda r: r.get("reliability_score", 0), reverse=True)

    seen_urls:   set  = set()
    seen_titles: list = []
    deduped:     list = []

    for item in all_items:
        url   = item.get("url", "")
        title = item.get("title", "").lower().strip()

        if url in seen_urls:
            continue

        if any(_jaccard(title, seen) > 0.75 for seen in seen_titles):
            continue

        deduped.append(item)
        seen_urls.add(url)
        seen_titles.append(title)

    removed = len(all_items) - len(deduped)
    if removed > 0:
        print(f"  🗑️  중복 {removed}개 제거")

    return {
        "scholar": [r for r in deduped if r["source"] == "scholar"],
        "x":       [r for r in deduped if r["source"] == "x"],
        "threads": [r for r in deduped if r["source"] == "threads"],
        "web":     [r for r in deduped if r["source"] == "web"],
    }


def _jaccard(a: str, b: str) -> float:
    """두 문자열의 단어 집합 간 자카드 유사도를 계산합니다."""
    sa, sb = set(a.split()), set(b.split())
    if not sa or not sb:
        return 0.0
    return len(sa & sb) / len(sa | sb)


print("✅ 중복 제거기 정의 완료")
print("   사용 가능한 함수: deduplicate(results)")

"""## 셀 6 — 초록 요약 (Abstract Summarizer)

Scholar에서 가져온 긴 논문 초록을 2~3 문장으로 압축합니다.

### 사용 모델: `sshleifer/distilbart-cnn-12-6`
- **BART** 기반 요약 모델의 경량화(distilled) 버전
- 크기: 약 1.2GB — 파이프라인에서 가장 무거운 단계
- 뉴스 기사 요약용으로 훈련됐지만 학술 초록에도 잘 작동합니다

### GPU 자동 감지
Colab GPU 런타임이면 `cuda`, 아니면 `cpu`로 자동 전환합니다.

### Lazy Loading
모델을 처음 사용할 때 한 번만 로드하고 이후엔 메모리에 유지합니다.
처리 조건: Scholar 결과 중 300자 이상인 초록만 요약합니다.

"""

_summarizer_model = None


def _get_summarizer():
    """요약 모델을 처음 호출 시 로드하고 이후엔 재사용합니다 (lazy loading)."""
    global _summarizer_model
    if _summarizer_model is None:
        try:
            import torch
            from transformers import pipeline as hf_pipeline

            device = "cuda" if torch.cuda.is_available() else "cpu"
            print(f"  🔄 요약 모델 로딩 중 (sshleifer/distilbart-cnn-12-6) on {device}...")

            _summarizer_model = hf_pipeline(
                "summarization",
                model="sshleifer/distilbart-cnn-12-6",
                device=device,
            )
            print("  ✅ 요약 모델 로드 완료")
        except ImportError:
            print("  ⚠️  transformers 없음 → 요약 단계 건너뜀")
    return _summarizer_model


def summarize_abstracts(results: dict) -> dict:
    """
    300자 이상인 Scholar 초록을 2~3 문장으로 요약합니다.
    transformers 미설치 시 조용히 건너뜁니다.
    """
    candidates = [
        r for r in results.get("scholar", [])
        if len(r.get("snippet", "")) > 300
    ]

    if not candidates:
        return results

    summarizer = _get_summarizer()
    if summarizer is None:
        return results

    for item in candidates:
        try:
            output = summarizer(
                item["snippet"],
                max_length=80,
                min_length=30,
                do_sample=False,
            )
            item["snippet"] = output[0]["summary_text"]
        except Exception as e:
            print(f"  ⚠️  '{item.get('title','')}' 요약 실패 (원본 유지): {e}")

    print(f"  ✅ {len(candidates)}개 초록 요약 완료")
    return results


print("✅ 초록 요약기 정의 완료")

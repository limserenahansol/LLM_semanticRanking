"""## 셀 3 — 의미론적 랭킹 (Semantic Ranker)

스크래핑된 결과를 **검색 주제와의 관련성** 순서대로 정렬합니다.

### 핵심 개념: 텍스트 임베딩

텍스트를 숫자 벡터로 변환하면 "의미가 얼마나 비슷한지"를 수학적으로 측정할 수 있습니다.

```
"LLM alignment"  →  [0.82, 0.11, 0.45, ...]  384차원 벡터
"AI safety"      →  [0.79, 0.13, 0.47, ...]  (비슷한 방향 → 높은 유사도)
"고양이 사료"     →  [0.02, 0.91, 0.03, ...]  (다른 방향 → 낮은 유사도)
```

두 벡터가 같은 방향을 가리키는 정도를 **코사인 유사도**로 측정합니다. 1.0이면 완전 동일, 0이면 무관합니다.

### Ranker (English)
- **Bi-encoder:** `BAAI/bge-base-en-v1.5` (query prefix per model card; passages unprefixed).
- **Lexical:** BM25 (`rank-bm25`) on the result batch + a light English keyword score.
- **Fusion:** `RANK_MERGE_MODE` — `rrf` (reciprocal rank fusion) or `weighted_norm` (min–max then weighted sum).
- **Optional:** top-`K` **cross-encoder** rerank (`ms-marco-MiniLM-L-6-v2`) on the fused list.
- **Fallback:** keyword-only if models fail.

"""

import numpy as np

# --- Bi-encoder (BGE English; GTE alternative e.g. Alibaba-NLP/gte-base-en-v1.5 in README) ---
_RANK_BI_ENCODER = None
RANK_BI_ENCODER_MODEL_NAME = "BAAI/bge-base-en-v1.5"
BGE_QUERY_PREFIX = "Represent this sentence for searching relevant passages: "

# rrf: fuse dense / BM25 / keyword rank lists. weighted_norm: min-max normalize each score vector.
RANK_MERGE_MODE = "rrf"  # "rrf" | "weighted_norm"
RRF_K = 60

_WEIGHTED_NORM_DENSE = 0.55
_WEIGHTED_NORM_BM25 = 0.35
_WEIGHTED_NORM_KEYWORD = 0.10

_CROSS_ENCODER = None
RANK_CROSS_ENCODER_MODEL = "cross-encoder/ms-marco-MiniLM-L-6-v2"
RANK_CROSS_ENCODER_TOP_K = 40
_CROSS_ENCODER_BLEND = 0.55

_RANK_KEYWORD_STOPWORDS = frozenset({
    "the", "a", "an", "and", "or", "but", "if", "then", "else", "when", "where",
    "in", "on", "at", "to", "for", "of", "with", "by", "from", "as", "into",
    "is", "was", "are", "were", "been", "be", "being", "am",
    "have", "has", "had", "having", "do", "does", "did", "doing",
    "will", "would", "could", "should", "may", "might", "must", "shall", "can",
    "not", "no", "nor", "only", "own", "same", "so", "than", "too", "very",
    "just", "also", "both", "each", "few", "more", "most", "other", "some", "such",
    "this", "that", "these", "those", "here", "there", "what", "which", "who", "whom",
    "whose", "why", "how", "all", "any", "every", "about", "above", "after", "again",
    "against", "before", "below", "between", "through", "during", "without", "within",
})


def _tokenize_keywords_en(text: str) -> set:
    if not text:
        return set()
    words = re.findall(r"[a-z][a-z0-9]*", text.lower())
    return {w for w in words if len(w) >= 2 and w not in _RANK_KEYWORD_STOPWORDS}


def _keyword_relevance_score(topic: str, doc: str) -> float:
    """Query-term recall on the document plus a small phrase-match bonus (English)."""
    tw = _tokenize_keywords_en(topic)
    if not tw:
        return 0.0
    dw = _tokenize_keywords_en(doc)
    if not dw:
        return 0.0
    overlap = len(tw & dw)
    recall = overlap / len(tw)
    denom = min(len(dw), max(len(tw) * 4, 12))
    density = overlap / denom if denom else 0.0
    score = 0.68 * recall + 0.32 * min(1.0, density)
    tl, dl = topic.lower().strip(), doc.lower()
    if len(tl) >= 10 and tl in dl:
        score = min(1.0, score + 0.14)
    elif overlap == len(tw) and len(tw) >= 2:
        score = min(1.0, score + 0.06)
    return round(min(1.0, score), 4)


def _result_document_text(r: dict) -> str:
    parts = [
        str(r.get("title") or ""),
        str(r.get("snippet") or ""),
        str(r.get("authors") or ""),
        str(r.get("venue") or ""),
    ]
    return " ".join(p for p in parts if p).strip()


def _bm25_tokenize(text: str) -> list:
    """Lowercase alphanumeric tokens for BM25 (and query)."""
    return re.findall(r"[a-z0-9]+", (text or "").lower()) or ["_"]


def _min_max_norm(vec: np.ndarray) -> np.ndarray:
    v = np.asarray(vec, dtype=np.float64)
    lo, hi = float(v.min()), float(v.max())
    if hi - lo < 1e-12:
        return np.ones_like(v) * 0.5
    return (v - lo) / (hi - lo)


def _scores_to_order(scores: np.ndarray) -> list:
    """Indices best-first (descending score)."""
    return [i for i, _ in sorted(enumerate(scores), key=lambda x: (-x[1], x[0]))]


def _rrf_fuse(orders: list, k: int) -> np.ndarray:
    """Reciprocal rank fusion over multiple best-first index lists."""
    n = max((max(o, default=-1) for o in orders), default=-1) + 1
    if n <= 0:
        return np.array([])
    acc = np.zeros(n, dtype=np.float64)
    for order in orders:
        for pos, doc_i in enumerate(order):
            acc[doc_i] += 1.0 / (k + pos + 1)
    return acc


def _get_bi_encoder():
    global _RANK_BI_ENCODER
    if _RANK_BI_ENCODER is None:
        from sentence_transformers import SentenceTransformer
        print(f"  🔄 Loading bi-encoder ({RANK_BI_ENCODER_MODEL_NAME})...")
        _RANK_BI_ENCODER = SentenceTransformer(RANK_BI_ENCODER_MODEL_NAME)
        print("  ✅ Bi-encoder ready")
    return _RANK_BI_ENCODER


def _get_cross_encoder():
    global _CROSS_ENCODER
    if _CROSS_ENCODER is None:
        from sentence_transformers import CrossEncoder
        print(f"  🔄 Loading cross-encoder ({RANK_CROSS_ENCODER_MODEL})...")
        _CROSS_ENCODER = CrossEncoder(RANK_CROSS_ENCODER_MODEL)
        print("  ✅ Cross-encoder ready")
    return _CROSS_ENCODER


def rank_by_relevance(topic: str, results: list) -> list:
    """
    Dense (BGE) + BM25 + keyword → RRF or normalized weighted sum → optional
    top-K cross-encoder rerank. Puts `raw_score` in [0,1] (min-max on fused scores).
    """
    if not results:
        return []

    topic_clean = (topic or "").strip()
    if not topic_clean:
        for r in results:
            r["raw_score"] = 0.0
            r["semantic_score"] = None
            r["keyword_score"] = 0.0
            r["bm25_score"] = None
            r["cross_encoder_score"] = None
        return results

    n = len(results)
    docs = [_result_document_text(r) for r in results]
    keyword_scores = np.array(
        [_keyword_relevance_score(topic_clean, d) for d in docs], dtype=np.float64
    )

    def _apply_keyword_only():
        for r, kw in zip(results, keyword_scores):
            r["semantic_score"] = None
            r["bm25_score"] = None
            r["keyword_score"] = round(float(kw), 4)
            r["cross_encoder_score"] = None
            r["raw_score"] = round(float(kw), 4)

    bm25_scores = np.zeros(n, dtype=np.float64)
    try:
        from rank_bm25 import BM25Okapi
        corpus_tokens = [_bm25_tokenize(d) for d in docs]
        bm25 = BM25Okapi(corpus_tokens)
        bm25_scores = np.array(
            bm25.get_scores(_bm25_tokenize(topic_clean)), dtype=np.float64
        )
    except ImportError:
        print("  ⚠️  rank-bm25 not installed → BM25 skipped in fusion")
    except Exception as e:
        print(f"  ⚠️  BM25 failed ({type(e).__name__}) → BM25 skipped")

    dense_scores = np.zeros(n, dtype=np.float64)
    try:
        from sentence_transformers import util
        model = _get_bi_encoder()
        q_text = BGE_QUERY_PREFIX + topic_clean
        topic_emb = model.encode(
            q_text,
            convert_to_tensor=True,
            normalize_embeddings=True,
        )
        texts = [d[:8192] if d else " " for d in docs]
        result_emb = model.encode(
            texts,
            convert_to_tensor=True,
            normalize_embeddings=True,
            show_progress_bar=False,
        )
        row = util.cos_sim(topic_emb, result_emb)[0]
        dense_scores = (
            row.detach().cpu().numpy()
            if hasattr(row, "detach")
            else np.asarray(row, dtype=np.float64)
        )
        dense_scores = np.clip(np.ravel(dense_scores), 0.0, 1.0)
    except ImportError:
        print("  ⚠️  sentence-transformers not installed → keyword-only ranking")
        _apply_keyword_only()
        return sorted(results, key=lambda r: r["raw_score"], reverse=True)
    except Exception as e:
        print(f"  ⚠️  Bi-encoder failed ({type(e).__name__}: {e}) → keyword-only")
        _apply_keyword_only()
        return sorted(results, key=lambda r: r["raw_score"], reverse=True)

    orders = [
        _scores_to_order(dense_scores),
        _scores_to_order(bm25_scores),
        _scores_to_order(keyword_scores),
    ]
    if RANK_MERGE_MODE == "rrf":
        fused = _rrf_fuse(orders, RRF_K)
    elif RANK_MERGE_MODE == "weighted_norm":
        nd = _min_max_norm(dense_scores)
        nb = _min_max_norm(bm25_scores)
        nk = np.clip(keyword_scores, 0.0, 1.0)
        fused = (
            _WEIGHTED_NORM_DENSE * nd
            + _WEIGHTED_NORM_BM25 * nb
            + _WEIGHTED_NORM_KEYWORD * nk
        )
    else:
        fused = _rrf_fuse(orders, RRF_K)

    # Optional cross-encoder on top-K by fused score
    prelim_order = _scores_to_order(fused)
    K = min(RANK_CROSS_ENCODER_TOP_K, n) if RANK_CROSS_ENCODER_TOP_K > 0 else 0
    ce_by_idx = {}
    if K > 0:
        try:
            ce = _get_cross_encoder()
            top_idx = prelim_order[:K]
            pairs = [
                (topic_clean, (docs[i] or " ")[:2000]) for i in top_idx
            ]
            ce_raw = np.array(
                ce.predict(pairs, show_progress_bar=False), dtype=np.float64
            )
            ce_n = _min_max_norm(ce_raw)
            fus_sub = np.array([fused[i] for i in top_idx], dtype=np.float64)
            fus_n = _min_max_norm(fus_sub)
            for j, idx in enumerate(top_idx):
                fused[idx] = float(
                    _CROSS_ENCODER_BLEND * ce_n[j]
                    + (1.0 - _CROSS_ENCODER_BLEND) * fus_n[j]
                )
                ce_by_idx[idx] = round(float(ce_raw[j]), 4)
            print(
                f"  ✅ Cross-encoder rerank applied to top {K} of {n} (blend={_CROSS_ENCODER_BLEND})"
            )
        except ImportError:
            print("  ⚠️  CrossEncoder unavailable → skipped rerank")
        except Exception as e:
            print(f"  ⚠️  Cross-encoder rerank skipped ({type(e).__name__}: {e})")

    fused_norm = _min_max_norm(fused)

    for i, r in enumerate(results):
        r["semantic_score"] = round(float(dense_scores[i]), 4)
        r["bm25_score"] = round(float(bm25_scores[i]), 4)
        r["keyword_score"] = round(float(keyword_scores[i]), 4)
        r["cross_encoder_score"] = ce_by_idx.get(i)
        r["raw_score"] = round(float(fused_norm[i]), 4)

    print(
        f"  ✅ Ranking: BGE dense + BM25 + keyword → {RANK_MERGE_MODE}"
        + (f" (RRF k={RRF_K})" if RANK_MERGE_MODE == "rrf" else "")
        + f" ({n} items)"
    )

    return sorted(results, key=lambda r: r["raw_score"], reverse=True)


print("✅ 의미론적 랭커 정의 완료")

"""## 셀 7 — 캐시 (Semantic Cache)

검색 기록을 SQLite DB에 저장하고, 같은 주제를 다시 검색하면 마지막 검색 이후의 새 결과만 가져옵니다.

### SQLite 데이터베이스
모든 기록은 단일 파일 `research_cache.db` 에 저장됩니다. 서버 설치가 필요 없습니다.

### 의미론적 쿼리 매칭
단순 문자열 비교 대신 **의미가 같은 쿼리를 같은 것으로 인식**합니다.

```
"LLM alignment"          →  벡터 [0.82, 0.11, ...]
"대형 언어 모델 정렬"     →  벡터 [0.79, 0.13, ...]  유사도 0.91 → 캐시 히트!
"large language model safety" → 벡터 [0.77, 0.15, ...]  유사도 0.88 → 캐시 히트!
```

**모델: `paraphrase-multilingual-MiniLM-L12-v2`** — 50개 이상 언어 지원, 한국어↔영어 교차 매칭 가능

### 델타 페칭
캐시 히트 시 `last_run` 날짜를 스크래퍼에 전달해 그 이후 결과만 수집합니다.
새 결과는 `is_new=True`, 기존 결과는 `is_new=False` 로 표시됩니다.

"""

import sqlite3
import json
import numpy as np
from datetime import datetime, timezone
from pathlib import Path
from typing import Optional

DB_PATH = Path("/content/research_cache.db")
SIMILARITY_THRESHOLD = 0.82
_cache_embed_model = None


def _get_cache_embed_model():
    """다국어 임베딩 모델을 처음 호출 시 로드합니다."""
    global _cache_embed_model
    if _cache_embed_model is None:
        try:
            from sentence_transformers import SentenceTransformer
            print("  🔄 다국어 임베딩 모델 로딩 중 (paraphrase-multilingual-MiniLM-L12-v2)...")
            _cache_embed_model = SentenceTransformer("paraphrase-multilingual-MiniLM-L12-v2")
            print("  ✅ 모델 로드 완료")
        except ImportError:
            print("  ⚠️  sentence-transformers 없음 → 정확한 문자열 매칭으로 대체")
    return _cache_embed_model


def _embed_query(text: str) -> Optional[np.ndarray]:
    m = _get_cache_embed_model()
    return m.encode(text, convert_to_numpy=True).astype(np.float32) if m else None


def _cosine(a: np.ndarray, b: np.ndarray) -> float:
    denom = np.linalg.norm(a) * np.linalg.norm(b)
    return float(np.dot(a, b) / denom) if denom else 0.0


def _get_conn():
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    conn.execute("PRAGMA journal_mode=WAL")
    return conn


def _init_db():
    with _get_conn() as conn:
        conn.executescript(
            "CREATE TABLE IF NOT EXISTS queries ("
            "    id INTEGER PRIMARY KEY AUTOINCREMENT,"
            "    topic TEXT NOT NULL UNIQUE,"
            "    embedding BLOB,"
            "    aliases TEXT DEFAULT '[]',"
            "    last_run TEXT NOT NULL,"
            "    run_count INTEGER DEFAULT 1,"
            "    created_at TEXT NOT NULL"
            ");"
            "CREATE TABLE IF NOT EXISTS results ("
            "    id INTEGER PRIMARY KEY AUTOINCREMENT,"
            "    query_id INTEGER NOT NULL REFERENCES queries(id),"
            "    source TEXT NOT NULL,"
            "    title TEXT, authors TEXT, date TEXT, snippet TEXT, url TEXT,"
            "    citations INTEGER DEFAULT 0, venue TEXT,"
            "    raw_score REAL DEFAULT 0,"
            "    reliability_score REAL DEFAULT 0,"
            "    reliability_tier TEXT DEFAULT 'uncertain',"
            "    is_new INTEGER DEFAULT 1,"
            "    first_seen TEXT NOT NULL,"
            "    UNIQUE(query_id, url)"
            ");"
        )

_init_db()


def find_similar_query(topic: str) -> Optional[dict]:
    """의미론적으로 유사한 기존 쿼리를 찾습니다."""
    emb = _embed_query(topic)
    if emb is None:
        return _get_exact_query(topic)

    with _get_conn() as conn:
        rows = conn.execute(
            "SELECT id, topic, embedding, aliases, last_run, run_count "
            "FROM queries WHERE embedding IS NOT NULL"
        ).fetchall()

    if not rows:
        return None

    best_score, best_row = 0.0, None
    for row in rows:
        score = _cosine(emb, np.frombuffer(row["embedding"], dtype=np.float32))
        if score > best_score:
            best_score, best_row = score, row

    if best_score < SIMILARITY_THRESHOLD or not best_row:
        return None

    print(f"  💾 캐시 히트: '{topic}' ≈ '{best_row['topic']}' (유사도: {best_score:.3f})")

    aliases = json.loads(best_row["aliases"] or "[]")
    if topic not in aliases:
        aliases.append(topic)
        with _get_conn() as conn:
            conn.execute(
                "UPDATE queries SET aliases=? WHERE id=?",
                (json.dumps(aliases, ensure_ascii=False), best_row["id"])
            )

    return _load_query(best_row["id"])


def _get_exact_query(topic: str) -> Optional[dict]:
    with _get_conn() as conn:
        row = conn.execute("SELECT * FROM queries WHERE topic=?", (topic,)).fetchone()
        if not row:
            return None
        return _load_query(row["id"])


def _load_query(qid: int) -> dict:
    with _get_conn() as conn:
        row     = conn.execute("SELECT * FROM queries WHERE id=?", (qid,)).fetchone()
        results = conn.execute(
            "SELECT * FROM results WHERE query_id=? ORDER BY reliability_score DESC", (qid,)
        ).fetchall()
        return {
            "query_id":  row["id"],
            "topic":     row["topic"],
            "aliases":   json.loads(row["aliases"] or "[]"),
            "last_run":  row["last_run"],
            "run_count": row["run_count"],
            "results":   [dict(r) for r in results],
        }


def save_to_cache(topic: str, processed: dict, canonical_id: Optional[int] = None) -> int:
    """처리된 결과를 SQLite에 저장합니다."""
    now  = datetime.now(timezone.utc).isoformat()
    emb  = _embed_query(topic)
    blob = emb.tobytes() if emb is not None else None

    with _get_conn() as conn:
        if canonical_id:
            conn.execute(
                "UPDATE queries SET last_run=?, run_count=run_count+1 WHERE id=?",
                (now, canonical_id)
            )
            qid = canonical_id
        else:
            ex = conn.execute("SELECT id FROM queries WHERE topic=?", (topic,)).fetchone()
            if ex:
                conn.execute(
                    "UPDATE queries SET last_run=?, run_count=run_count+1, embedding=? WHERE id=?",
                    (now, blob, ex["id"])
                )
                qid = ex["id"]
            else:
                cur = conn.execute(
                    "INSERT INTO queries(topic, embedding, aliases, last_run, created_at) VALUES(?,?,?,?,?)",
                    (topic, blob, "[]", now, now)
                )
                qid = cur.lastrowid

        for source in ["scholar", "x", "threads", "web"]:
            for item in processed.get(source, []):
                conn.execute(
                    "INSERT INTO results"
                    "    (query_id,source,title,authors,date,snippet,url,citations,venue,"
                    "     raw_score,reliability_score,reliability_tier,is_new,first_seen)"
                    " VALUES(?,?,?,?,?,?,?,?,?,?,?,?,?,?)"
                    " ON CONFLICT(query_id,url) DO UPDATE SET"
                    "    reliability_score=excluded.reliability_score,"
                    "    reliability_tier=excluded.reliability_tier,"
                    "    raw_score=excluded.raw_score, is_new=0",
                    (qid, item.get("source"), item.get("title"), item.get("authors"),
                     item.get("date"), item.get("snippet"), item.get("url"),
                     item.get("citations", 0), item.get("venue"),
                     item.get("raw_score", 0.0), item.get("reliability_score", 0.0),
                     item.get("reliability_tier", "uncertain"),
                     1 if item.get("is_new", True) else 0, now)
                )
        conn.commit()

    return qid


def get_search_history() -> list:
    """저장된 검색 기록을 반환합니다."""
    with _get_conn() as conn:
        rows = conn.execute(
            "SELECT q.id, q.topic, q.aliases, q.last_run, q.run_count, COUNT(r.id) AS result_count "
            "FROM queries q LEFT JOIN results r ON r.query_id=q.id "
            "GROUP BY q.id ORDER BY q.last_run DESC LIMIT 20"
        ).fetchall()
    return [{**dict(r), "aliases": json.loads(dict(r).get("aliases") or "[]")} for r in rows]


